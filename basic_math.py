# This program does basic math.


def main():
    print("7-3={}".format(7 - 3))
    print("2+4={}".format(2 + 4))
    print("8*6={}".format(8 * 6))
    print("4/2={}".format(4 / 2))
    print("1/2={}".format(1 / 2))
    print("2+5*3={}".format(2 + 5 * 3))
    print("2^3={}".format(2 ^ 3))  # bitwise operator
    print("2**3={}".format(2**3))  # exponent
    print("16**0.5={}".format(16**0.5))  # square root


if __name__ == "__main__":
    main()
